CREATE TABLE IF NOT EXISTS requests_friend_list (
	sender_id INTEGER REFERENCES users(user_id),
	recipient_id INTEGER REFERENCES users(user_id),
	CONSTRAINT not_selfi CHECK (sender_id != recipient_id)
	);